package lesson2;

public class HomeWork1 {

    public static void main(String[] args) {
        /**
         * С помощью цикла For, напечатать на консоль
         * только четные от -100 до -50
         */

        for (int i = -100; i <= -50; i = i + 2) {
            System.out.println(i);
        }
    }
}
